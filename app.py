# BAR GRAPH
# Generate a bar plot to compare the average salary of male and female employees
import pandas as pd
import matplotlib as mpl
import matplotlib.pyplot as plt
df = pd.read_csv("C:/Users/ayesh/Downloads/Employee data.csv") #print(df)
average_salary = df.groupby('gender')['salary'].mean() #calculating average of specific columns using mean()
average_salary.plot(kind="bar", color = ['red','black'])
plt.xlabel("GENDER")
plt.ylabel("SLARY")
plt.title("AVERAGE SALARY OF MALE AND FEMALE")
plt.show()


