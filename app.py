# PIE CHART
# Generate a pie chart to visualize the distribution of educational backgrounds among employees.
import pandas as pd
import matplotlib as mpl
import matplotlib.pyplot as plt
df = pd.read_csv("C:/Users/ayesh/Downloads/Employee data.csv")
df.groupby('educ')
ed_counts = df['educ'].value_counts()
ed_counts.plot(kind='pie',
                figsize=(5, 6),
                autopct='%1.1f%%', # add in percentages
                startangle=90,         
                pctdistance=10.12,    # the ratio between the center of each pie slice and the start of the text generated by autopct 
            )
plt.title('EDUCATIONAL BACKGROUND')
plt.axis('equal') # Sets the pie chart to look like a circle.
plt.show()

